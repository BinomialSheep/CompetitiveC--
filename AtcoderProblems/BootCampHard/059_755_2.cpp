struct Solver {
  void solve() {
    /* input */
    int N;
    cin >> N;

    /* solve */
    int ans = 0;

    auto check = [&](ll m) -> bool {
      set<int> st;
      while (m > 0) {
        st.insert(m % 10);
        m /= 10;
      }
      return st.size() == 3;
    };

    auto dfs = [&](auto f, ll m) -> void {
      if (m > N) return;
      if (check(m)) ans++;
      f(f, 10 * m + 3);
      f(f, 10 * m + 5);
      f(f, 10 * m + 7);
    };
    dfs(dfs, 0);

    /* output */
    cout << ans << endl;
  }
};

int main() {
  int ts = 1;
  rep(ti, ts) {
    Solver solver;
    solver.solve();
  }
  return 0;
}