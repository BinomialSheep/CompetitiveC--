struct Solver {
  void solve() {
    /* input */
    ll S;
    cin >> S;

    /* solve */
    ll X1 = 0, Y1 = 0, X2 = 1e9, Y2 = 1;
    ll X3 = X2 - S % X2;
    ll Y3 = S / X2 + 1;
    if (S == (ll)1e18) {
      printf("0 0 %lld 0 %lld %lld\n", X2, X2, X2);
      return;
    }

    /* output */
    printf("%lld %lld %lld %lld %lld %lld\n", X1, Y1, X2, Y2, X3, Y3);

    // debug
    assert(abs(X2 * Y3 - X3 * Y2) == S);
  }
};

