// 普通の素因数分解 O(√N)
template <typename T>
vector<pair<T, T>> calcPrimeFactorSimple(T n) {
  vector<pair<T, T>> ret;
  for (T i = 2; i * i <= n; i++) {
    if (n % i != 0) continue;
    T tmp = 0;
    while (n % i == 0) {
      tmp++;
      n /= i;
    }
    ret.emplace_back(i, tmp);
  }
  if (n != 1) ret.emplace_back(n, 1);
  return ret;
}

// シンプルな約数列挙(O(√N))
// 1e6以下の最大の約数の個数は240、1e9で1344,
// 1e12で6720, 1e18で103680
vector<long long> calc_divisors(long long N) {
  // 答えを表す集合
  vector<long long> res;

  // 各整数 i が N の約数かどうかを調べる
  for (long long i = 1; i * i <= N; ++i) {
    // i が N の約数でない場合はスキップ
    if (N % i != 0) continue;

    // i は約数である
    res.push_back(i);

    // N ÷ i も約数である (重複に注意)
    if (N / i != i) res.push_back(N / i);
  }
  // 約数を小さい順に並び替えて出力
  sort(res.begin(), res.end());
  return res;
}

struct Solver {
  void solve() {
    /* input */
    LL(A, B);
    using mint = modint998244353;

    /* solve */
    vpll ps = calcPrimeFactorSimple(A);

    mint product = 1;
    bool isEven = false;
    if (B % 2 == 0) isEven = true;
    for (auto [base, exponent] : ps) {
      product *= (mint)exponent * B + 1;
      if (exponent % 2) isEven = true;
    }

    /* output */
    if (isEven) {
      out((product * B / 2).val());
    } else {
      out(((product * B - 1) / 2).val());
    }
  }
};

