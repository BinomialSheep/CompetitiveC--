struct Solver {
  void solve() {
    int T;
    cin >> T;
    rep(ti, T) {
      /* input */
      int N, K;
      cin >> N >> K;
      map<int, int> mp;
      rep(i, N) {
        int S;
        cin >> S;
        mp[S]++;
      }

      /* solve */
      string ans = "YES";
      if (N > K * 2) ans = "NO";
      for (auto p : mp)
        if (p.second > 2) ans = "NO";

      /* output */
      printf("Case #%d: %s\n", ti + 1, ans.c_str());
    }
  }
};

